        <ol>
          <li>Gather multiple publicly available COVID-19 data sets</li>
          <li>Construct both small training and large data sets from (1)</li>
          <li>Preprocess the data utilizing machine learning methodology (normalization, missing data, encoding character/string data…)</li>
          <li>Analyze small training sets utilizing principal component analysis (PCA) and autoencoders (AE) models</li>
          <li>Compare results</li>
		  <li>Analyze full sets utilizing PCA and AE models</li>
		  <li>Compare results</li>
		  <li>Iterate AE model on 4-7 until results are valid based on literature currently available</li>
		  <li>Distinguish different scenarios in the spread of the virus in the countries, which have successfully overcome the peak and find the reasons behind those differences</li>
		  <li>Utilizing data sets from 4 and 6 run through traditional machine learning models, such as: regression, nonlinear regression, logistic regression and k-nearest means in order to find the explanation of the different characteristics of the curves for different countries. The methods will be applied to the whole data sets as well as to the datasets including only the countries whose trends show that they have passed the peak of the pandemic spread in order to find the explanation to the different character of the curves</li>
		  <li>Utilizing data sets from (4 and 6) run more complex deep learning methods on the data sets (AE (unsupervised), multilayer perceptron (MLP) artificial neural networks (supervised), convolutional neural networks (CNN, supervised/semisupervised/supervised) and recurrent neural networks with long-short term memory (RNN-LSTM, supervised, semisupervised) and generative adversarial networks (GAN, supervised)</li>
		  <li>Compare results from 10 and 11</li>
		  <li>Compare with current pandemic data and make short term and long term predictions from tuned models</li>
		  <li>Relying on the results of 9, 10 and 11 propose possible measures to effectively quench the spread of similar viral infections in the future</li>
        </ol>
		
